public class operatorAritmatika {
  public static void main(String[] args) {
    System.out.printf("20 + 6 = %.2f\n", (20 + 6.0));
    System.out.printf("20 - 6 = %.2f\n", (20 - 6.0));
    System.out.printf("20 * 6 = %.2f\n", (20 * 6.0));
    System.out.printf("20 / 6 = %.2f\n", (20 / 6.0));
    System.out.printf("20 %% 6 = %.2f\n", (20 % 6.0));
  }
}
